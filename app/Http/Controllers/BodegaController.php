     */
    public function store(Request $request)
    {
        $Bodega = new Bodega();

        $Bodega->cod_tienda = $request->input('tienda');
        $Bodega->direccion_bodega = $request->input('direccion_bodega');
        $Bodega->estado = 0;
        $Bodega->save();

        return 'Guardado';
    }

